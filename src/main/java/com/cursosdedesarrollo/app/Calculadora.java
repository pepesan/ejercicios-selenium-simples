package com.cursosdedesarrollo.app;

class Calculadora {

    Calculadora (){

    }

    public Integer add(Integer s1, Integer s2){
        return s1+s2;
    }

    public Integer sub(Integer s1, Integer s2){
        return s1-s2;
    }

    public int divide(int i, int i1) {
        return i/i1;
    }
}